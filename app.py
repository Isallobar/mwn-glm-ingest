def glm_poll_loop():
    """Background thread — polls GLM S3 every POLL_INTERVAL seconds."""
    log.info("GLM poll loop started")
    while True:
        try:
            dt = datetime.now(timezone.utc)
            flashes = None
            used_bucket = None

            for bucket in GOES_BUCKETS:
                key = latest_glm_key(bucket, dt)
                if key is None:
                    log.warning(f"No GLM files found in {bucket}")
                    continue

                fname = key.split('/')[-1]
                try:
                    s_part = [p for p in fname.split('_') if p.startswith('s')][0]
                    year   = int(s_part[1:5])
                    doy    = int(s_part[5:8])
                    hour   = int(s_part[8:10])
                    minute = int(s_part[10:12])
                    second = int(s_part[12:14])
                    file_dt = datetime(year, 1, 1, hour, minute, second,
                                       tzinfo=timezone.utc) + timedelta(days=doy - 1)
                    age = (dt - file_dt).total_seconds()
                    if age > MAX_AGE_SEC:
                        log.info(f"Most recent GLM file is {age:.0f}s old — skipping")
                        continue
                except Exception:
                    file_dt = dt

                flashes = parse_glm_file(bucket, key)
                used_bucket = bucket
                break

            if flashes is not None:
                clusters = cluster_flashes(flashes)
                with _lock:
                    _state['clusters']    = clusters
                    _state['file_time']   = file_dt
                    _state['fetch_time']  = dt
                    _state['source']      = used_bucket
                    _state['file_count'] += 1
                    _state['flash_count'] += len(flashes)
                    _state['error']       = None
                log.info(f"GLM updated: {len(clusters)} clusters from {len(flashes)} flashes")
            else:
                with _lock:
                    _state['error'] = 'No recent GLM file found'

        except Exception as e:
            log.error(f"GLM poll error: {e}", exc_info=True)
            with _lock:
                _state['error'] = str(e)

        time.sleep(POLL_INTERVAL)


def sti_poll_loop():
    """Background thread — polls NEXRAD STI from S3 every STI_POLL_INTERVAL seconds."""
    log.info(f"STI poll loop started — {len(NEXRAD_SITES)} sites, every {STI_POLL_INTERVAL}s")

    while True:
        try:
            start_time = time.time()
            all_cells = []
            sites_checked = 0
            sites_with_data = 0
            errors = 0

            with _sti_lock:
                _sti_state['poll_count'] += 1

            for site in NEXRAD_SITES:
                sites_checked += 1
                try:
                    key = get_latest_sti_key(site['id'])
                    if not key:
                        continue

                    # Log first few successful key finds for debugging
                    if sites_with_data < 3:
                        log.info(f"  [STI] Found key for {site['id']}: {key}")

                    # Skip if already processed
                    with _sti_lock:
                        if _sti_state['last_file_by_site'].get(site['id']) == key:
                            existing = [c for c in _sti_state['cells'] if c['siteId'] == site['id']]
                            all_cells.extend(existing)
                            continue

                    # Check age
                    age = get_file_age_sec(key)
                    if age > STI_MAX_AGE_SEC:
                        if sites_with_data < 3:
                            log.info(f"  [STI] {site['id']} file too old ({age:.0f}s)")
                        continue

                    cells = parse_sti_file(key, site['id'])
                    if cells:
                        sites_with_data += 1
                        all_cells.extend(cells)
                        with _sti_lock:
                            _sti_state['last_file_by_site'][site['id']] = key

                except Exception as e:
                    errors += 1
                    log.debug(f"STI error for {site['id']}: {e}")

            # Also keep recent cells from sites we didn't update
            now_dt = datetime.now(timezone.utc)
            with _sti_lock:
                for cell in _sti_state['cells']:
                    key = f"{cell['siteId']}_{cell.get('cellId', '')}_{cell['lat']},{cell['lon']}"
                    existing_keys = set(
                        f"{c['siteId']}_{c.get('cellId', '')}_{c['lat']},{c['lon']}"
                        for c in all_cells
                    )
                    if key not in existing_keys:
                        try:
                            cell_age = (now_dt - datetime.fromisoformat(cell['time'])).total_seconds()
                            if cell_age < STI_MAX_AGE_SEC:
                                all_cells.append(cell)
                        except (ValueError, KeyError):
                            pass

            with _sti_lock:
                _sti_state['cells'] = all_cells
                _sti_state['last_poll'] = datetime.now(timezone.utc).isoformat()
                _sti_state['sites_checked'] = sites_checked
                _sti_state['sites_with_data'] = sites_with_data
                _sti_state['error_count'] += errors

            elapsed = time.time() - start_time
            log.info(f"STI poll #{_sti_state['poll_count']}: "
                     f"{sites_checked} sites, {sites_with_data} with data, "
                     f"{len(all_cells)} cells ({elapsed:.1f}s)")

        except Exception as e:
            log.error(f"STI poll error: {e}", exc_info=True)
            with _sti_lock:
                _sti_state['error_count'] += 1

        time.sleep(STI_POLL_INTERVAL)


