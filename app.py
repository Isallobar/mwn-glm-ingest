@app.route('/sti')
def sti_endpoint():
    """
    Returns current NEXRAD SCIT storm cell data as GeoJSON.
    Optional: ?bounds=south,west,north,east to filter by bounding box.
    """
    with _sti_lock:
        cells = list(_sti_state['cells'])

    # Optional bounding box filter
    bounds = request.args.get('bounds')
    if bounds:
        try:
            parts = [float(x) for x in bounds.split(',')]
            if len(parts) == 4:
                south, west, north, east = parts
                cells = [c for c in cells
                         if south <= c['lat'] <= north and west <= c['lon'] <= east]
        except (ValueError, TypeError):
            pass

    features = []
    for c in cells:
        features.append({
            'type': 'Feature',
            'geometry': {'type': 'Point', 'coordinates': [c['lon'], c['lat']]},
            'properties': {
                'cellId':         c.get('cellId'),
                'siteId':         c.get('siteId'),
                'motionDir':      c.get('motionDir'),
                'motionSpeedKts': c.get('motionSpeedKts'),
                'motionSpeedMph': c.get('motionSpeedMph'),
                'maxDbz':         c.get('maxDbz'),
                'vil':            c.get('vil'),
                'echoTop':        c.get('echoTop'),
                'echoBase':       c.get('echoBase'),
                'time':           c.get('time'),
            },
        })

    return jsonify({
        'type': 'FeatureCollection',
        'features': features,
        'meta': {
            'storm_count':    len(features),
            'total_cells':    len(_sti_state['cells']),
            'last_poll':      _sti_state.get('last_poll'),
            'poll_count':     _sti_state.get('poll_count', 0),
            'source':         'NEXRAD Level III STI (Product 58)',
        },
    })


