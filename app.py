def get_latest_sti_key(site_id: str) -> str | None:
    """Find the most recent STI file for a NEXRAD site on S3."""
    now = datetime.now(timezone.utc)
    # S3 bucket uses 3-letter codes without K prefix (KLIX → LIX)
    s3_site = site_id[1:] if site_id.startswith('K') and len(site_id) == 4 else site_id
    prefix = f"{s3_site}_STI_{now.strftime('%Y_%m_%d')}"

    try:
        resp = s3.list_objects_v2(
            Bucket=STI_BUCKET, Prefix=prefix, MaxKeys=50
        )
        keys = [obj['Key'] for obj in resp.get('Contents', []) if '_STI_' in obj['Key']]
        if not keys:
            # Try yesterday if early UTC
            if now.hour < 2:
                yesterday = now - timedelta(days=1)
                prefix2 = f"{s3_site}_STI_{yesterday.strftime('%Y_%m_%d')}"
                resp2 = s3.list_objects_v2(Bucket=STI_BUCKET, Prefix=prefix2, MaxKeys=50)
                keys = [obj['Key'] for obj in resp2.get('Contents', []) if '_STI_' in obj['Key']]
            if not keys:
                return None
        # Keys sort alphabetically = chronologically, take the last one
        keys.sort()
        return keys[-1]
    except Exception as e:
        log.debug(f"STI list error for {site_id}: {e}")
        return None


def get_file_age_sec(key: str) -> float:
    """Extract timestamp from STI filename and compute age."""
    # Format: KLIX_STI_2026_03_08_01_35_40
    parts = key.split('_')
    if len(parts) < 8:
        return float('inf')
    try:
        y, mo, d = int(parts[2]), int(parts[3]), int(parts[4])
        h, mi, se = int(parts[5]), int(parts[6]), int(parts[7])
        file_dt = datetime(y, mo, d, h, mi, se, tzinfo=timezone.utc)
        return (datetime.now(timezone.utc) - file_dt).total_seconds()
    except (ValueError, IndexError):
        return float('inf')


def parse_sti_file(key: str, site_id: str) -> list[dict]:
    """
    Fetch and parse a NEXRAD Level III STI file from S3.
    
    STI tabular format:
      STM  AZ/RAN  MOVEMENT   DBZM  HT   TOP  FCST MVMT
      ID  DEG/NM   DEG/KTS          KFT  KFT   DEG/NM  
      F4  242/ 67  242/ 22    53   6.3  22.4  245/ 67
    
    MOVEMENT is DIR/SPD (direction degrees / speed knots)
    """
    site = NEXRAD_SITE_MAP.get(site_id)
    if not site:
        return []

    site_lat, site_lon = site['lat'], site['lon']

    try:
        obj = s3.get_object(Bucket=STI_BUCKET, Key=key)
        data = obj['Body'].read()
    except Exception as e:
        log.debug(f"STI fetch error {key}: {e}")
        return []

    cells = []
    import re

    try:
        # Decode binary as ASCII — the Tabular Alphanumeric Block is readable text
        # embedded within the binary Level III structure
        text_data = data.decode('ascii', errors='replace')
        
        # Split on common line endings (binary data may use \r\n, \n, or \r)
        lines = re.split(r'[\r\n]+', text_data)

        for line in lines:
            line = line.strip()
            if not line or len(line) < 10:
                continue

            # Match STI cell line: ID  AZ/RAN  DIR/SPD  DBZ ...
            # Cell ID is 1-2 uppercase letters + 1 digit (A0, B1, F4, AA1, etc.)
            # AZ/RAN: azimuth(deg) / range(nm)
            # DIR/SPD: movement direction(deg) / speed(kts)  
            # DBZ: max reflectivity
            m = re.match(
                r'\s*([A-Z]{1,2}\d)\s+'                    # Cell ID
                r'(\d{1,3})\s*/\s*(\d{1,3})\s+'           # AZ / RAN
                r'(\d{1,3})\s*/\s*(\d{1,3})\s+'           # DIR / SPD (MOVEMENT)
                r'(\d{1,3})',                               # DBZM
                line
            )
            if not m:
                continue

            try:
                cell_id = m.group(1)
                azimuth = int(m.group(2))
                range_nm = int(m.group(3))
                direction = int(m.group(4))    # MOVEMENT direction in degrees
                speed_kts = int(m.group(5))    # MOVEMENT speed in knots
                max_dbz = int(m.group(6))
            except (ValueError, IndexError):
                continue

            # Sanity checks
            if azimuth > 360 or range_nm > 250 or direction > 360 or speed_kts > 150:
                continue

            # Convert range/azimuth to lat/lon
            range_km = range_nm * 1.852
            az_rad = math.radians(azimuth)
            lat = site_lat + (range_km / 111.32) * math.cos(az_rad)
            lon = site_lon + (range_km / (111.32 * math.cos(math.radians(site_lat)))) * math.sin(az_rad)

            # Parse optional echo top from remaining columns
            echo_top = None
            echo_base = None
            remaining = line[m.end():]
            nums = re.findall(r'(\d+\.?\d*)', remaining)
            if len(nums) >= 2:
                try:
                    echo_base = round(float(nums[0]) * 1000)  # kft → ft
                    echo_top = round(float(nums[1]) * 1000)
                except (ValueError, IndexError):
                    pass

            cells.append({
                'cellId':         cell_id,
                'siteId':         site_id,
                'lat':            round(lat, 3),
                'lon':            round(lon, 3),
                'motionDir':      direction,
                'motionSpeedKts': speed_kts,
                'motionSpeedMph': round(speed_kts * 1.15078),
                'maxDbz':         max_dbz,
                'vil':            None,
                'echoTop':        echo_top,
                'echoBase':       echo_base,
                'time':           datetime.now(timezone.utc).isoformat(),
            })

        if cells:
            log.info(f"  [STI] {site_id}: {len(cells)} cells from {key}")

    except Exception as e:
        log.warning(f"STI parse error for {key}: {e}")

    return cells


