def start_threads():
    """Start both poll threads. Called from __main__ or gunicorn post_fork."""
    glm_thread = threading.Thread(target=glm_poll_loop, daemon=True)
    glm_thread.start()
    log.info("GLM poll thread started")

    sti_thread = threading.Thread(target=sti_poll_loop, daemon=True)
    sti_thread.start()
    log.info("STI poll thread started")


